//Formatting Numbers

//toString() Method:
let d = 369;
console.log(d);
console.log(d.toString());
console.log((300+69).toString());

//to Exponential() Method:
let f = 9.656;
console.log(f);
console.log(f.toExponential(2));
console.log(f.toExponential(4));
console.log(f.toExponential(5));

//toFixed() Method:
let m = 8.658;
console.log(m);
console.log(m.toFixed());
console.log(m.toFixed(2));
console.log(m.toFixed(4));

//toPrecision() Method:
let n = 9.656;
console.log(n);
console.log(n.toPrecision());
console.log(n.toPrecision(2));
console.log(n.toPrecision(4));
console.log(n.toPrecision(3));

//valueOf() Method:
//it returns the primitive value of the specified object.

let x = 555;
console.log(x);
console.log(x.valueOf());
console.log((555).valueOf());

